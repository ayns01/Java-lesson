package Lab_Chapter2;

public class Thirtyone {

    public static void main(String[] args) {
        System.out.print("number  ");
        System.out.print("square  ");
        System.out.println("cube");

        for (int i=0; i<11; i++) {
            System.out.print(i + "       ");
            System.out.print(i * i + "       ");
            System.out.print(i * i * i);
            System.out.println();
        }

    }
}
